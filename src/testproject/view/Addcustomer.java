    private void AddCUstomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddCUstomerActionPerformed
        // TODO add your handling code here:
        String Name=name.getText();
        String Phoneno=phone.getText();
        String Address=address.getText();
        String City=city.getText();
        String State=state.getText();
        String Email=email.getText();
        
        try {
            String validateCustomer = validateCustomer (Name,Phoneno,Address,City,State,Email);
            if(validateCustomer.equals("validated"))
            {
                Customer c = new Customer();
                c.setName(Name);
                c.setPhoneno(Phoneno);
                c.setState(State);
                c.setAddress(Address);
                c.setCity(City);
                c.setEmail(Email);
                
                String saveCustomer = new CustomerService().saveCustomer(config.S , c);
                
                config.T.commit();
                JOptionPane.showMessageDialog(this, saveCustomer, "Add Customer Form" , JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
                new Homepage().setVisible(true);
                
            }
                    
                        
                    
        } 
        catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_AddCUstomerActionPerformed

