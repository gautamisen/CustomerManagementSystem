public String validateCustomer(String name,String phoneno,String address,String city,String state,String email )
{
    if(name.isEmpty())
    {
        JOptionPane.showMessageDialog(this, "Name is required", "AddCustomer Form", JOptionPane.ERROR_MESSAGE);
    }
    else if(phoneno.isEmpty())
    {
        JOptionPane.showMessageDialog(this, "Phone No is required", "AddCustomer Form", JOptionPane.ERROR_MESSAGE);
    }
    else if(address.isEmpty())
    {
        JOptionPane.showMessageDialog(this, "Address is required", "AddCustomer FORM", JOptionPane.ERROR_MESSAGE);
    }
    else if(city.isEmpty())
    {
        JOptionPane.showMessageDialog(this, "City is required", "AddCustomer Form", JOptionPane.ERROR_MESSAGE);
    }
    else if(state.isEmpty())
    {
        JOptionPane.showMessageDialog(this, "State is required", "AddCustomer Form ", JOptionPane.ERROR_MESSAGE);
    }
    else if(email.isEmpty())
    {
        JOptionPane.showMessageDialog(this, "Email is required", "AddCustomer Form",JOptionPane.ERROR_MESSAGE);
    }
    
    return "validated";
}

